// while loop
var b = 10;

while (b > 0) {
    console.log(b + " Kegs on the wall. w");
    b--;
}

// Do while loop
var c = 10;

do {
    console.log(c + " Kegs on the wall. dw");
    c--;
}
while (c > 0);

// for loop
for (i = 10; i > 0; i--) {
    console.log(i + " Kegs on the wall. f");
}